package statistics;

import java.util.Date;

/**
 * Abstract class that represents a single log of a request
 * @author lineker
 *
 */
public class LogRequest {
	private int mId;
	private int mSongId;
	private int mFeedbackId;
	private String mDeviceType;
	private Date mRequestDate;
	
	
	/**
	 * Setter Id
	 * @param id
	 */
	public void setId(int id)
	{
		mId = id;
	}
	
	/**
	 * Getter Id
	 * @return
	 */
	public int getId()
	{
		return mId;
	}
	
	/**
	 * Setter Id
	 * @param id
	 */
	public void setSongId(int id)
	{
		mSongId = id;
	}
	
	/**
	 * Getter Id
	 * @return
	 */
	public int getSongId()
	{
		return mSongId;
	}
	
	/**
	 * Setter Id
	 * @param id
	 */
	public void setFeedbackId(int id)
	{
		mFeedbackId = id;
	}
	
	/**
	 * Getter Id
	 * @return
	 */
	public int getFeedbackId()
	{
		return mFeedbackId;
	}
	
	/**
	 * Setter Title
	 * @param title
	 */
	public void setDeviceType(String type)
	{
		mDeviceType = type;
	}
	
	/**
	 * Getter Text
	 * @return
	 */
	public String getDeviceType()
	{
		return mDeviceType;
	}
	
	/**
	 * Setter Date
	 * @param text
	 */
	public void setRequestDate(Date date)
	{
		mRequestDate = date;
	}
	
	/**
	 * Getter Date
	 * @return
	 */
	public Date getRequestDate()
	{
		return mRequestDate;
	}
}
